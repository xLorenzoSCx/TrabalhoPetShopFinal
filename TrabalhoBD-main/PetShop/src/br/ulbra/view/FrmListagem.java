    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tabelaUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();

        for (Usuario p : udao.read()) {
            modelo.addRow(new Object[]{
                p.getNomeUsu(),
                p.getEmailUsu(),
                p.getFoneUsu(),
                p.getCpfUsu()

            });
        }
    }

